import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOHeadProps {
  title?: string;
  description?: string;
  keywords?: string;
  canonicalUrl?: string;
  ogImage?: string;
  ogType?: string;
}

export function SEOHead({ 
  title = 'PDF Security Editor', 
  description = 'Secure PDF redaction tool with local processing',
  keywords = 'PDF, redaction, security, privacy, document',
  canonicalUrl = '/',
  ogImage = '/favicon.svg',
  ogType = 'website'
}: SEOHeadProps) {
  const fullUrl = canonicalUrl.startsWith('http') ? canonicalUrl : `https://secureredact.tech${canonicalUrl}`;
  
  return (
    <Helmet>
      <title>{title}</title>
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <link rel="canonical" href={fullUrl} />
      
      {/* Open Graph */}
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:type" content={ogType} />
      <meta property="og:url" content={fullUrl} />
      <meta property="og:image" content={ogImage} />
      
      {/* Twitter */}
      <meta name="twitter:card" content="summary_large_image" />
      <meta name="twitter:title" content={title} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={ogImage} />
      
      {/* Additional SEO */}
      <meta name="robots" content="index, follow" />
      <meta name="author" content="PDF Security Editor" />
    </Helmet>
  );
}